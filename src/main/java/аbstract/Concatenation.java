package аbstract;

public class Concatenation {
    // Метод выводит на экран союз "и"
    public void and() {
        System.out.print(" and ");
    }

    // Метод выводит на экран "пробел"
    public void space() {
        System.out.print(" ");
    }

    // Метод выводит на экран точку
    public void dot() {
        System.out.print(".");
    }
}

